    private void GenerateBait()
    {
        // 生成位置の調整
        var fixPosX = (numHorizontal - 1) * generateInterval / 2;
        var fixPosZ = (numVertical - 1) * generateInterval / 2;

        for (int i = 0; i < numVertical; i++)
        {
            for (int j = 0; j < numHorizontal; j++)
            {
                Vector3 generatePos = new Vector3( j * generateInterval - fixPosX, generatePosY, i * -generateInterval + fixPosZ);   // 生成する位置
                GameObject _bait = null;   // 生成するオブジェクト格納用

                // 生成するオブジェクトの決定
                if (baitInfo[i, j] == 1) _bait = bait[0];
                else if (baitInfo[i, j] == 2) _bait = bait[1];

                if (_bait != null)
                {
                    GameObject _baits = Instantiate(_bait, generatePos, Quaternion.identity);

                    if(isFPS) _baits.transform.localScale = new Vector3(generateScale, generateScale, generateScale) * 5;
                    else _baits.transform.localScale *= generateScale;
                    _baits.transform.SetParent(this.transform);
                }
            }
        }
    }
